@FragmentScope
public class PublicPresenter extends BasePresenter<PublicContract.Model, PublicContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public PublicPresenter(PublicContract.Model model, PublicContract.View rootView) {
        super(model, rootView);
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }

    public void getWxTabs(){
        mModel.getWxTabs()
                .subscribeOn(Schedulers.io())
                .observeOn(AndroidSchedulers.mainThread())
                .compose(RxLifecycleUtils.bindToLifecycle(mRootView))
                .subscribe(new ErrorHandleSubscriber<WanAndroidResponse<List<Tab>>>(mErrorHandler) {
                    @Override
                    public void onNext(WanAndroidResponse<List<Tab>> data) {
                        mRootView.setWxTabs(data.getData());
                    }
                });
    }
}
